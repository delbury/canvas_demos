/**
 * JSDoc 的 demo
 * @abstract 该成员（一般指父类的方法）必须在继承的子类中实现（或重写） // 别名：@virtual
 * 
 * @access <private|protected|public> 指定该成员的访问级别（私有 private，公共 public，或保护 protected）
 * @access private 等价于 @private;
 * @access protected 等价于 @protected;
 * @access public 等价于 @public
 * 
 * @alias <aliasNamepath> 标记成员有一个别名
 * 
 * @augments <namepath> 指名这个子类继承至哪个父类，后面需要加父类名 // 别名：@extends
 * 
 * @author <name> [<emailAddress>] 指定项目的作者
 * 
 * @borrows <that namepath> as <this namepath> 这个对象使用另一个对象的某些东西
 */